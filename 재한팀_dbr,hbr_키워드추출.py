from keybert import KeyBERT
from kiwipiepy import Kiwi
import pandas as pd


# 형태소 분석기 초기화
kiwi = Kiwi()

# KeyBERT 모델 초기화
kw_model = KeyBERT(model="paraphrase-multilingual-MiniLM-L12-v2")


def extract_nng_text(text):
    """
    입력 텍스트에서 NNG(일반명사)만 추출하여
    KeyBERT 입력용 문자열로 변환하는 함수
    """
    if pd.isna(text):
        return ""

    text = str(text).strip()

    if text == "":
        return ""

    words = []

    for token in kiwi.tokenize(text):
        word = token.form
        pos = token.tag

        # NNG: 일반명사만 사용
        if pos == "NNG":

            # 한 글자 단어 제거
            if len(word) <= 1:
                continue

            # 불용어 제거
            if word in STOPWORDS:
                continue

            words.append(word)

    return " ".join(words)


def extract_keybert_keywords(text):
    # 값이 비어 있거나 NaN인 경우 빈 리스트 반환
    if pd.isna(text):
        return []

    # NNG 일반명사만 남긴 텍스트 생성
    nng_text = extract_nng_text(text)

    # NNG 추출 결과가 비어 있으면 빈 리스트 반환
    if nng_text == "":
        return []

    # KeyBERT 키워드 추출
    keywords = kw_model.extract_keywords(
        nng_text,
        keyphrase_ngram_range=(1, 1),  # 1개 단어 키워드만 추출
        stop_words=None,               # 불용어는 위에서 직접 제거했으므로 None
        top_n=10                       # 문서 1개당 상위 10개 키워드 추출
    )

    return keywords


