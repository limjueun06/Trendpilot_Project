import requests
import pandas as pd
from getpass import getpass
from datetime import datetime
from zoneinfo import ZoneInfo
from math import ceil

# ============================================================
# 1. 인증키 입력
# ============================================================
SERVICE_KEY = getpass("공공데이터포털 인증키를 입력하세요: ")

# ============================================================
# 2. 지원사업 공고 정보 API URL
# ============================================================
url = "https://apis.data.go.kr/B552735/kisedKstartupService01/getAnnouncementInformation01"

# ============================================================
# 3. API 한 페이지 호출 함수
# ============================================================
def fetch_page(page, per_page=100):
    params = {
        "ServiceKey": SERVICE_KEY,
        "page": page,
        "perPage": per_page,
        "returnType": "json",
        "rcrt_prgs_yn": "Y"   # 모집진행여부 Y
    }

    response = requests.get(url, params=params, timeout=30)

    print(f"{page}페이지 상태코드:", response.status_code)

    # 응답이 정상 JSON인지 확인
    try:
        data = response.json()
    except Exception:
        print("JSON 변환 실패")
        print(response.text[:1000])
        raise

    return data

# ============================================================
# 4. 첫 페이지 호출
# ============================================================
per_page = 100
first_data = fetch_page(page=1, per_page=per_page)

print("응답 key:", first_data.keys())
print("현재 페이지 데이터 수:", first_data.get("currentCount"))
print("전체 데이터 수:", first_data.get("totalCount"))

# ============================================================
# 5. 전체 페이지 수 계산
# ============================================================
total_count = first_data.get("totalCount", 0)
total_pages = ceil(total_count / per_page)

print("전체 페이지 수:", total_pages)

# ============================================================
# 6. 전체 페이지 반복 수집
# ============================================================
all_items = first_data.get("data", [])

for page in range(2, total_pages + 1):
    page_data = fetch_page(page=page, per_page=per_page)
    all_items.extend(page_data.get("data", []))

# ============================================================
# 7. DataFrame 변환
# ============================================================
df = pd.DataFrame(all_items)

print("전체 수집 공고 수:", len(df))
display(df.head())

# ============================================================
# 8. 오늘 날짜 기준 모집 가능한 공고 필터링
