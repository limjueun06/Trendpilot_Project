today = pd.Timestamp(datetime.now(ZoneInfo("Asia/Seoul")).date())

print("오늘 날짜:", today.date())

# 날짜 컬럼 변환
df["pbanc_rcpt_bgng_dt"] = pd.to_datetime(df["pbanc_rcpt_bgng_dt"], errors="coerce")
df["pbanc_rcpt_end_dt"] = pd.to_datetime(df["pbanc_rcpt_end_dt"], errors="coerce")

# 모집진행여부 정리
df["rcrt_prgs_yn"] = df["rcrt_prgs_yn"].astype(str).str.upper()

# 오늘 기준 접수 가능한 공고만 필터링
