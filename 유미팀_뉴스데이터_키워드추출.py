# ============================================================
# 1. BERTopic 토픽 키워드 추출용 토크나이저
# ============================================================
# clean_content 문장은 그대로 BERTopic에 입력하되,
# 토픽 대표 단어를 뽑을 때는 Kiwi 형태소 분석 + 불용어 제거를 적용한다.
# 이 코드에서는 일반명사(NNG)만 토픽 키워드 후보로 사용한다.
# ============================================================

def kiwi_tokenizer(text):
    tokens = []

    for token in kiwi.tokenize(text):
        word = token.form
        pos = token.tag

        # NNG: 일반명사
        # NNP: 고유명사
        # VV : 동사
        # VA : 형용사
        if pos in ["NNG", "NNP", "VV", "VA"]:

            # 한 글자 단어 제거
            if len(word) <= 1:
                continue

            # 불용어 제거
            if word in STOPWORDS:
                continue

            tokens.append(word)

    return tokens


# ============================================================
# 2. BERTopic 입력 데이터 준비
# ============================================================
# df_400의 clean_content 컬럼에서 비어 있지 않은 문장만 사용
