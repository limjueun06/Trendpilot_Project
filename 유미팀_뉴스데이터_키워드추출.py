# ============================================================
# 명사 추출 + 불용어 제거 함수
# ============================================================
# clean_content 컬럼의 문장 데이터에서 명사 중심 단어만 추출하고,
# 불용어 사전에 포함된 단어를 제거하여 분석용 텍스트를 생성한다.
# ============================================================

def remove_stopwords_and_extract_nouns(text):
    # 값이 비어 있거나 NaN인 경우 빈 문자열 반환
    if pd.isna(text):
        return ""

    text = str(text)

    words = []

    # Kiwi 형태소 분석 수행
    for token in kiwi.tokenize(text):
        word = token.form
        pos = token.tag

        # NNG: 일반명사
        # NNP: 고유명사
        # VV: 동사
        # VA: 형용사
        if pos in ["NNG", "NNP", "VV", "VA"]:

            # 한 글자 단어 제거
            if len(word) <= 1:
                continue

            # 불용어 제거
            if word in STOPWORDS:
                continue

            words.append(word)

    return " ".join(words)


